let x = document.getElementById('hideimage');
function snowing(){
	document.body.style.background = "url('Pixelart/snow.gif') #ACBEBE";
    x.style.display = "run-in";
	}
			
function stop_snowing(){
	document.body.style.background = "#F4F4EF";
    x.style.display = "run-in";
	x.style.animation = "none";
	}